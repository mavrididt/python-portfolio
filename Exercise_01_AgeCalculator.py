
# ΑΣΚΗΣΗ 1: Υπολογιστής ηλικίας

# Ζητάμε από τον χρήστη να εισάγει το έτος γέννησης
birth_year = input("Ποιο έτος γεννήθηκες; ")
birth_year = int(birth_year)  # Μετατρέπουμε την είσοδο σε αριθμό

# Υπολογίζουμε την ηλικία
current_year = 2025
age = current_year - birth_year

# Εκτυπώνουμε το αποτέλεσμα
print("Η ηλικία σου είναι:", age)
